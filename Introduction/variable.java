public class variable {
    public static void main(String[] args) {
       byte a=100;
       short b=10;
       int c=123;
       int d=-987;
       long e=123456l;
       float f=10.2f;
       double g=123456.123d;
       boolean h=true;
       boolean i=false;
       char j=56;
       char k='o';
       
       System.out.println("Byte values:-"+a);
       System.out.println("short values:-"+b);
       System.out.println("Integer values:-"+c);
       System.out.println("integer values:-"+d);
       System.out.println("Long values:--"+e);
       System.out.println("float values:-"+f);
       System.out.println("Double values:-"+g);
       System.out.println("Boolean values:-"+h);
       System.out.println("Boolean values :-"+i);
       System.out.println("character values:"+j);
       System.out.println("character values:--"+k);

       
    }
    
}
