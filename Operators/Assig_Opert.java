package Operators;

public class Assig_Opert {
    public static void main(String[] args) {
        int s=24;
        int t=13;
        System.out.println("s*t:"+(s*t));
        System.out.println("s+t:"+(s+t));
        System.out.println("s-t"+(s-t));
        System.out.println("s/t"+(s/t));
    }
    
}
