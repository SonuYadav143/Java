package Operators;

public class Rel_Operator {
    public static void main(String[] args) {
        int s=22;
        int t=13;
        System.out.println("First s:-"+s);
        System.out.println("Second t:-"+t);
        System.out.println("S > T:"+(s>t));
        System.out.println("s<t:"+(s<t));
        System.out.println("s>=t:"+(s>=t));
        System.out.println("s<=t:"+(s<=t));
        System.out.println("s==t:"+(s==t));
        System.out.println("s!=t:"+(s!=t));

    }
    
}
