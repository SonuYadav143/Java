package Operators;

public class Arith_Add {
    public static void main(String[] args) {
        int a=10;
        int b=15;
        int sum;
        sum=a+b;

        System.out.println("Add Two number:-"+sum);

        sum=a*b;
        System.out.println("Multiple:-"+sum);

        sum=a/b;
        System.out.println("Division of two number:-"+sum);

        sum=a-b;
        System.out.println("Substraction two number:-"+sum);

        sum=a%b;
        System.out.println("Moduls:-"+sum);
    }
    
}
