package Array_Lecture;

public class b_2_Array_using_loop {
    public static void main(String[] args){
        int [] array = new int [5];
        array[0] =111;
        array[1] =222;
        array[2] =333;
        array[3] =444;
        array[4] =555;

        for(int i=0; i<=5; i++){
            System.out.println(array[i]);
        }
    }
}
